package am.ik.yavi;

public class Range {
	private final int from;
	private final int to;

	public Range(int from, int to) {
		this.from = from;
		this.to = to;
	}

	public int getFrom() {
		return from;
	}

	public int getTo() {
		return to;
	}

	public boolean isToGreaterThanFrom() {
		return this.to > this.from;
	}
}
